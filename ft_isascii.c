int	ft_isascii(char *c_ascii)
{
	int	i;

	i = 0;
	while (c_ascii[i] != '\0')
	{
		if (!(c_ascii[i] >= 0 && c_ascii[i] <= 127))
			return (0);
		i++;
	}
	return (1);
}
