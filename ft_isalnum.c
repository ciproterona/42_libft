int	ft_isalnum(char *alnum)
{
	int	i;

	i = 0;
	while (alnum[i] != '\0')
	{
		if (!((alnum[i] >= 'a' && alnum[i] <= 'z')
				|| (alnum[i] >= 'A' && alnum[i] <= 'Z')
				|| (alnum[i] >= '0' && alnum[i] <= '9')))
			return (0);
		i++;
	}
	return (1);
}
