int	ft_isalnum(char alnum)
{
	while (alnum != '\0')
	{
		if (!((alnum >= 'a' && alnum <= 'z')
				|| (alnum >= 'A' && alnum <= 'Z')
				|| (alnum >= '0' && alnum <= '9')))
			return (0);
		alnum++;
	}
	return (1);
}
