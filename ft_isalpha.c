int	ft_isalpha(char *letter)
{
	int	i;

	i = 0;
	while (letter[i] != '\0')
	{
		if (!((letter[i] >= 'a' && letter[i] <= 'z')
				|| (letter[i] >= 'A' && letter[i] <= 'Z')))
			return (0);
		i++;
	}
	return (1);
}
