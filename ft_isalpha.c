int	ft_isalpha(char letter)
{
	while (letter != '\0')
	{
		if (!((letter >= 'a' && letter <= 'z')
				|| (letter >= 'A' && letter <= 'Z')))
			return (0);
		letter++;
	}
	return (1);
}
